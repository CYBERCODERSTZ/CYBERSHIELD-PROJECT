                <h5><i>Tanzania Communications Regulatory Authority (TCRA)</i></h5>
                 <p>
                Tanzania Communications Regulatory Authority (TCRA) is a statutory regulatory body responsible for regulating the electronic and postal communication sector in Tanzania. It was established under the Tanzania Communications Regulatory Authority Act, No.12 of 2003, which merged the Tanzania Communications Commission (TCC) and the Tanzania Broadcasting Commission (TBC).
                </p>
                <p>
                 Tanzania Communications Commission (TCC) was established in 1993 with the mandate of regulating the telecommunications and postal services. Tanzania Broadcasting Commission (TBC) was established in 1993 with the mandate of regulating the broadcasting sector.<br>
                  Following technological advancement, there was a need to streamline government services to enhance public service delivery, minimizing bureaucracy 
                </p>
                <p>
                  and increase efficiency, the two regulatory bodies (TCC and TBC) were merged in 2003 to form Tanzania Communications Regulatory Authority (TCRA). The Authority became operational on 1st November, 2003 and effectively took over the functions of the merged two Commissions. This is mandated by section 4 (1) to (7) of the TCRA Act, 2003. Visit official TCRA website at <i><a href=" https://tcra.go.tz/">TCRA</a></i>, <a href="https://tcra.go.tz/services/statistics"> TCRA communication statistics </a>, <a href="https://tcra.go.tz/pages/reports">Annual TCRA report </a>  visit also <a href="https://www.tzcert.go.tz/">Tanzania Computer Emergency Response Team (TZ-CERT) official page </a>
                </p>
