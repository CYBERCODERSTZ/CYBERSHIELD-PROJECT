                <h2 class="list-sidebar">International standards</h2>
                <h5><i>ISO/IEC 27001, An international standard for information security management systems (ISMS)</i></h5>
                 <p>
                  What is ISO/IEC 27001?<br>
                  ISO/IEC 27001 is the world's best-known standard for information security management systems (ISMS). It defines requirements an ISMS must meet.<br>
                  The ISO/IEC 27001 standard provides companies of any size and from all sectors of activity with guidance for establishing, implementing, maintaining and continually improving an information security management system.
                 Conformity with ISO/IEC 27001 means that an organization or business has put in place a system to manage risks related to the security of data owned or handled by the company, and that this system respects all the best practices and principles enshrined in this International Standard. Buy the standard at official ISO website at <i> <a href="https://www.iso.org/standard/27001"> Standard </a></i> 
                </p>

                <h5><i>NIST Cybersecurity Framework</i></h5>
                <p>
                  NIST Cybersecurity Framework is a set of guidelines for mitigating organizational cybersecurity risks, published by the US National Institute of Standards and Technology (NIST) based on existing standards, guidelines, and practices.[1] The framework "provides a high level taxonomy of cybersecurity outcomes and a methodology to assess and manage those outcomes",[2] in addition to guidance on the protection of privacy and civil liberties in a cybersecurity context.[3] It has been translated to many languages, and is used by several governments[4] and a wide range of businesses and organizations.[5][6][7]<br>
                 A 2016 study found that 70% of organizations surveyed see the NIST Cybersecurity Framework as a popular best practice for computer security, but many note that it requires significant investment.[8] read more about NIST at <i> <a href="https://www.nist.gov"> NIST </a></i> 
                 Get your NIST framework documentation for review from our database here <i><a href="  https://www.cybershield/resources /nistframework ">Cybershield Resource</a></i>
                </p>
                <h5><i>CIS Controls (Center for Internet Security Controls)</i></h5>
                <p>
                  The CIS Critical Security Controls (CIS Controls) are a prescriptive, prioritized, and simplified set of best practices that you can use to strengthen your cybersecurity posture. Today, thousands of cybersecurity practitioners from around the world use the CIS Controls and/or contribute to their development via a community consensus process. Adventure more about CIS at Cybershield Resource<br>
                 Get your CIS security controls documentation for review from our database here <i><a href="https://www.cybershield/resources /CIS">CIS security</a> </i>
